    function displayHomeProducts(data)
    {
        let container=document.getElementById("home");
        container.innerHTML=null;
        data.forEach((el)=>{
            let homediv=document.createElement("div");
            homediv.setAttribute("id","popupgp");
            homediv.addEventListener("click",function()
            {
                popuphomepagediv(el);
            })
            let homeimage=document.createElement("img");
            homeimage.src=el.home_image;
            homeimage.setAttribute("class","imagehover");
            let div1=document.createElement("div");
            div1.setAttribute("id","ratingdiv")
            let hometitle=document.createElement("h3");
            hometitle.innerText=el.home_product_title;
            let homeproduct=document.createElement("h4");
            homeproduct.innerText=el.home_pro_details;
            homeproduct.style.color="grey";
            homeproduct.style.fontSize="14px";
            homeproduct.style.fontFamily="Brandon Text,Arial,sans-serif";
            let proprice=document.createElement("h3");
            proprice.innerText=el.Price;
            let prorating=document.createElement("img");
            prorating.setAttribute("class","ratingimg");
            prorating.src=el.pro_rating;
            let prodelivery=document.createElement("h3");
            prodelivery.style.color="grey";
            prodelivery.innerText=el.home_order;
            let productsavg=document.createElement("h3");
            productsavg.innerText=el.products_av;
            productsavg.setAttribute("class","svg");
            div1.append(prorating,productsavg)
            homediv.append(homeimage,hometitle,homeproduct,proprice,div1,prodelivery);
            container.append(homediv);
        })
    }
